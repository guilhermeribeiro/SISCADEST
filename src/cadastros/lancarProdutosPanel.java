    private void removerLancarProducaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerLancarProducaoActionPerformed
        try {
                int row = tableLancarProd.getSelectedRow();

                String sql = "select * from lancamento_producao Where lprd_id = " + Integer.parseInt(tableLancarProd.getModel().getValueAt(row, 0).toString());
                conexao_lancar_producao.executaSQL(sql);
                conexao_lancar_producao.resultset.first();
                String nome = "Deletar o Cliente : " + conexao_lancar_producao.resultset.getString("lprd_nome") + " ?";
                int opcao_escolhida = JOptionPane.showConfirmDialog(null, nome, "Exclusão ", JOptionPane.YES_NO_OPTION);
                if (opcao_escolhida == JOptionPane.YES_OPTION) {
                    sql = "DELETE FROM lancamento_producao Where lprd_id =" + Integer.parseInt(tableLancarProd.getModel().getValueAt(row, 0).toString());
                    int conseguiu_excluir = conexao_lancar_producao.statement.executeUpdate(sql);
                    if (conseguiu_excluir == 1) {
                        JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso");
                        //atualiza o ResultSet
                        conexaoLancamentoProducao();
                        preencherJTabelLancamentoProducao();
                        
                        java.util.Date dt = new java.util.Date();
        
                        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy HH:mm");

                        String currentTime = sdf.format(dt);
                        
                        jt_Cod.setText("");
                        jt_Desc.setText("");
                        jt_Obs.setText("");
                        jt_Prod.setText("");
                        jt_dtNasc1.setText(currentTime);
                    }
                } else {
                    return;
                }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro a tentar excluir o registro..." + erro);
        }
    }//GEN-LAST:event_removerLancarProducaoActionPerformed

