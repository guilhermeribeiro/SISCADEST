    private void tableLancarProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableLancarProdMouseClicked
        removerLancarProducao.setEnabled(true);
        //salvarLancamentoProducao.setEnabled(false);
        
        Point click = new Point(evt.getX(), evt.getY());

        int column = tableLancarProd.columnAtPoint(click);
        int row = tableLancarProd.rowAtPoint(click);
        String value = String.valueOf(tableLancarProd.getValueAt(row, 0));

        try {
            conexao_lancar_producao.resultset.first();
            String igual = "n";
            while (igual == "n") {
                String pesquisado = conexao_lancar_producao.resultset.getString("lprd_id");//.substring(0,(tamanho_pesquisa));
                if (pesquisado.equals(value)) {
                    igual = "s";
                } else {
                    conexao_lancar_producao.resultset.next();
                }
            }
            
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy HH:mm");
            
            String dbData = sdf.format(conexao_lancar_producao.resultset.getTimestamp(5));
            
            
            jt_Cod.setText(conexao_lancar_producao.resultset.getString("lprd_id"));
            jt_Prod.setText(conexao_lancar_producao.resultset.getString("lprd_nome"));
            jt_Desc.setText(conexao_lancar_producao.resultset.getString("lprd_descricao"));
            jt_Obs.setText(conexao_lancar_producao.resultset.getString("observacoes"));
            jt_dtNasc1.setText(dbData);
            
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Não conseguiu localizar via digitação, erro = " + erro);
        }
        
    }//GEN-LAST:event_tableLancarProdMouseClicked

