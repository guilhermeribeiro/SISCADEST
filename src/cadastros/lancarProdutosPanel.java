    public void preencherJTabelLancamentoProducao() {
        tableLancarProd.getColumnModel().getColumn(0).setPreferredWidth(100);
        tableLancarProd.getColumnModel().getColumn(1).setPreferredWidth(100);
        tableLancarProd.getColumnModel().getColumn(2).setPreferredWidth(100);
        tableLancarProd.getColumnModel().getColumn(3).setPreferredWidth(100);
        
        modelolancarproducao = (DefaultTableModel) tableLancarProd.getModel();
        modelolancarproducao.setNumRows(0);
        
        DefaultTableCellRenderer numeros =
                new DefaultTableCellRenderer() {
                    
                    public void setValue(Object value) {
                        //setBackground(new Color(238, 238, 238));
                        setFont(new Font("Arial", Font.BOLD /*+ Font.ITALIC*/, 12));
                        super.setValue(value);
                    }
                };
        tableLancarProd.getColumnModel().getColumn(1).setCellRenderer(numeros);
        try {            
            while (conexao_lancar_producao.resultset.next()) {
                    modelolancarproducao.addRow(new Object[]{
                    conexao_lancar_producao.resultset.getString("lprd_id"),
                    conexao_lancar_producao.resultset.getString("lprd_nome"),
                    conexao_lancar_producao.resultset.getString("lprd_descricao"),
                    conexao_lancar_producao.resultset.getString("observacoes"),
                    conexao_lancar_producao.resultset.getString("lprd_data")});
            }
           
            conexao_lancar_producao.resultset.first();
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro ao listar no JTable " + erro);
        }
    }

